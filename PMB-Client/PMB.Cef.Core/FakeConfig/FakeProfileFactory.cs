        private static List<string> GenerateAvailableFonts()
        {
            HashSet<string> source = new HashSet<string>();
            List<string> list = _allFonts.ToList();
            int count = list.Count;
            int int32 = Convert.ToInt32((double) count * 0.6);
            for (int index = 0; index < list.Count; ++index)
            {
                if (Randomizer.Between(0, count) < int32)
                    source.Add(list[index]);
            }

            return source.ToList<string>();
        }

        private static double GenerateRandomInt()
        {
            return (double) Randomizer.Between(1000, 9999) * 0.0001;
        }

        public static string GenerateUserAgent(WindowsVersion winVersion, bool isX64)
        {
            return "Mozilla/5.0 (" + WinVersions[winVersion] + GetX64String(isX64) +
                   ") AppleWebKit/537.36 (KHTML, like Gecko) Chrome/" + ChromeBuildVersion.GetRandValue() +
                   " Safari/537.36";
        }

        private static string GetX64String(bool isX64)
        {
            if (!isX64)
                return string.Empty;
            return "; WOW64; x64";
        }

        public static List<T> GetAllEnumValues<T>(Type type)
        {
            if (type == (Type) null)
                throw new ArgumentNullException("type == null");
            if (!type.IsEnum)
                throw new InvalidEnumArgumentException(type.Name + " is not Enum");
            return Enum.GetValues(type).Cast<T>().ToList<T>();
        }

        private static string GetMd5Hash(string value)
        {
            byte[] hash = MD5.Create().ComputeHash(Encoding.ASCII.GetBytes(value));
            StringBuilder stringBuilder = new StringBuilder();
            foreach (byte num in hash)
                stringBuilder.Append(num.ToString("x2"));
            return stringBuilder.ToString();
        }

        private static double GenerateBaseLatencyValue()
        {
            return double.Parse(
                string.Format("0,1{0}{1}{2}", (object) Randomizer.Between(0, 3), (object) Randomizer.Between(0, 99999),
                    (object) Randomizer.Between(0, 9999999)), (IFormatProvider) CultureInfo.GetCultureInfo("ru-Ru"));
        }
